#include <stdio.h>
int suma(int a, int b)
{
    return a + b;
}

int resta(int a, int b)
{
    return a - b;
}
int multiplicar(int a, int b)
{
    return a * b;
}
int dividir(int a, int b)
{
    return a / b;
}
int mayor (int a, int b){

    if (a<b){ 
        return a;
    }else{
        return b;
    }
}
